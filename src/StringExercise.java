public class StringExercise {
    public static void main(String[] args) {

        String message1 = "\nWe don't need no education\n" +
                "We don't need no thought control";
        System.out.println(message1);

        String message2 = "\nCheck \"this\" out!, \"s inside of \"s!";
        System.out.println(message2);

        String message3 = "\nIn windows, the main drive is usually C:\\";
        System.out.println(message3);

        String message4 = "\nI can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(message4);
    }
}
